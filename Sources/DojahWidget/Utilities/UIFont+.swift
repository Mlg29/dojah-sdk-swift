import UIKit

extension UIFont {
    class func primaryBlack(_ size: CGFloat = 14) -> UIFont {
        UIFont.systemFont(ofSize: size)
    }
    
    class func primaryBold(_ size: CGFloat = 14) -> UIFont {
        UIFont.systemFont(ofSize: size)
    }
    
    class func primaryRegular(_ size: CGFloat = 14) -> UIFont {
        UIFont.systemFont(ofSize: size)
    }
}
